export function getDailyIndex(quotes: DailyQuote[]): number {
  const now = new Date();
  const startOfYear = new Date(now.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((now.getTime() - startOfYear.getTime()) / 86400000);
  return dayOfYear % quotes.length;
}

export function getQuotes(lang: string): DailyQuote[] {
  return lang === 'ar' ? QUOTES_AR : QUOTES_EN;
}
